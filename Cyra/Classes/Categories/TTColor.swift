import UIKit

extension UIColor
{
	class func backgroundGray() -> UIColor
	{
		return UIColor(red: 230.0/255.0, green: 230.0/255.0, blue: 230.0/255.0, alpha: 1.0)
	}

	class func textGray() -> UIColor
	{
		return UIColor(red: 53.0/255.0, green: 53.0/255.0, blue: 53.0/255.0, alpha: 1.0)
	}
	
	class func cyraPink() -> UIColor
	{
		return UIColor(red: 253.0/255.0, green: 103.0/255.0, blue: 108.0/255.0, alpha: 1.0)
	}
}
